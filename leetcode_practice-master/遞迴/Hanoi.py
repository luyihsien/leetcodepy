def Hanoi(n,a,b,c):#數量，起點，中點，終點#移動是上部n-1與下部1是此題重要策略
    if n==0:
        return #不是一定要傳東西回去，也不是傳None
    elif n>0:
        Hanoi(n-1,a,c,b)#在Hanoi(1,a,b,c)時具備了移動一塊磚到目的地的能力#將這份能力透過參數代入順序的改變讓他往中點放，這樣就可以實施n-1移動的策略在Hanoi(2)
        print('{}--->{}'.format(a,c))#印出來等同移了故不回傳
        Hanoi(n-1,b,a,c)
Hanoi(3,'a','b','c')#用較小再運用運算解較大，故通常有return#重要點是都用同一個函數內的程式碼(同樣套路)

